// Helper function to get current template
export const getCurrentTemplate = () => {
  const businessType = APP_CONFIG.BUSINESS.BUSINESS_TYPE.toLowerCase().replace(/\s+/g, '_');
  return APP_CONFIG.TEMPLATES[businessType.toUpperCase()] || APP_CONFIG.TEMPLATES.RETAIL_STORE;
};

// Helper function to format currency
export const formatCurrency = (amount) => {
  const { SYMBOL, DECIMAL_PLACES, THOUSAND_SEPARATOR, DECIMAL_SEPARATOR } = APP_CONFIG.CURRENCY;
  
  if (isNaN(amount) || amount === null || amount === undefined) {
    return `${SYMBOL} 0${DECIMAL_SEPARATOR}${'0'.repeat(DECIMAL_PLACES)}`;
  }

  const fixedAmount = parseFloat(amount).toFixed(DECIMAL_PLACES);
  const [integer, decimal] = fixedAmount.split('.');
  
  // Add thousand separators
  const formattedInteger = integer.replace(/\B(?=(\d{3})+(?!\d))/g, THOUSAND_SEPARATOR);
  
  return `${SYMBOL} ${formattedInteger}${DECIMAL_SEPARATOR}${decimal}`;
};

// Helper function to get theme colors
export const getThemeColor = (section) => {
  const colorMap = {
    dashboard: APP_CONFIG.BRANDING.DASHBOARD_COLOR,
    sales: APP_CONFIG.BRANDING.SALES_COLOR,
    inventory: APP_CONFIG.BRANDING.INVENTORY_COLOR,
    reports: APP_CONFIG.BRANDING.REPORTS_COLOR,
    settings: APP_CONFIG.BRANDING.SETTINGS_COLOR,
  };
  
  return colorMap[section] || APP_CONFIG.BRANDING.PRIMARY_COLOR;
};

